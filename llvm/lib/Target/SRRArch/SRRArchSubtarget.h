#ifndef LLVM_LIB_TARGET_SRRARCH_SRRARCHSUBTARGET_H
#define LLVM_LIB_TARGET_SRRARCH_SRRARCHSUBTARGET_H

#include "SRRArchFrameLowering.h"
#include "SRRArchISelLowering.h"
#include "SRRArchInstrInfo.h"
#include "SRRArchSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "SRRArchGenSubtargetInfo.inc"

namespace llvm {

class SRRArchSubtarget : public SRRArchGenSubtargetInfo {
public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  SRRArchSubtarget(const Triple &TargetTriple, StringRef Cpu,
                   StringRef FeatureString, const TargetMachine &TM,
                   const TargetOptions &Options, CodeModel::Model CodeModel,
                   CodeGenOptLevel OptLevel);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  SRRArchSubtarget &initializeSubtargetDependencies(StringRef CPU,
                                                    StringRef FS);

  void initSubtargetFeatures(StringRef CPU, StringRef FS);

  bool enableMachineScheduler() const override { return true; }

  const SRRArchInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const SRRArchRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const SRRArchTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const SRRArchSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  SRRArchInstrInfo InstrInfo;
  SRRArchFrameLowering FrameLowering;
  SRRArchTargetLowering TLInfo;
  SRRArchSelectionDAGInfo TSInfo;
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_SRRARCH_SRRARCHSUBTARGET_H
